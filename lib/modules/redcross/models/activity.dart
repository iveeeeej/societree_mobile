class Activity {
  final String title;
  final String description;
  final DateTime date;
  final int hours;
  final String location;
  final int points;
  final bool isVerified;

  Activity({
    required this.title,
    required this.description,
    required this.date,
    required this.hours,
    required this.location,
    required this.points,
    required this.isVerified,
  });
}

